/**
 * Dynamically translates any English HTML text on the landing page into the targeted language,
 * by executing safe, exact match substring replacement on raw text nodes.
 */
export function getTranslatedLandingHTML(html: string, lang: Language): string {
  if (lang === 'en') {
    return html;
  }

  const translationsForLang = homeTranslations[lang as Exclude<Language, 'en'>];
  if (!translationsForLang) {
    return html;
  }

  let translatedHTML = html;

  // We sort keys by length descending to avoid short keys breaking long sentences during replacement
  const sortedKeys = Object.keys(translationsForLang).sort((a, b) => b.length - a.length);

  for (const englishPhrase of sortedKeys) {
    const translatedPhrase = translationsForLang[englishPhrase];
    // Replace all occurrences of the raw English phrase in the HTML body
    // Escape special characters in the key for regex
    const escapedKey = englishPhrase.replace(/[-\/\\^$*+?.()|[\]{}]/g, '\\$&');
    const regex = new RegExp(escapedKey, 'g');
    translatedHTML = translatedHTML.replace(regex, translatedPhrase);
  }

  return translatedHTML;
}
